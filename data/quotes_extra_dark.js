const PREFIX = '酒狐悄悄话: '

const TEMPLATES = [
  (a, b, c) => `${a}，${b}。${c}`,
  (a, b, c) => `${b}，${a}。${c}`,
  (a, b, c) => `${a}。${c}，${b}`,
  (a, b, c) => `${c}，${a}。${b}`,
]

function buildCategory(name, entries) {
  const lines = []

  for (const template of TEMPLATES) {
    for (const [a, b, c] of entries) {
      lines.push(PREFIX + template(a, b, c))
    }
  }

  if (lines.length !== 20) {
    throw new Error(`[quotes_extra_dark] 分类 ${name} 生成数量异常: ${lines.length}`)
  }

  if (new Set(lines).size !== lines.length) {
    throw new Error(`[quotes_extra_dark] 分类 ${name} 出现重复语录`)
  }

  return lines
}

